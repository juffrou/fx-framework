package org.juffrou.fx.error;

public class NodeBuildingException extends RuntimeException {

	private static final long serialVersionUID = -6274871906352804152L;

	public NodeBuildingException() {
		super();
	}

	public NodeBuildingException(String message, Throwable cause) {
		super(message, cause);
	}

	public NodeBuildingException(String message) {
		super(message);
	}

	
}
